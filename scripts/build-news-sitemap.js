#!/usr/bin/env node
/**
 * Generate the Google News sitemap (/news-sitemap.xml) from the Astro news
 * content collection. Google News sitemaps may only list articles published in
 * the LAST 48 HOURS, so this filters by pubDate and is safe to run on every build.
 *
 * Reads front-matter from news-app/src/content/news/*.md.
 */
'use strict';
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const DIR = path.join(ROOT, 'news-app', 'src', 'content', 'news');
const OUT = path.join(ROOT, 'news-sitemap.xml');
const SITE = 'https://goldpricetools.com';
const PUBLICATION = 'GoldPriceTools';
const WINDOW_MS = 48 * 60 * 60 * 1000;

function frontmatter(md) {
  const m = md.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  if (!m) return {};
  const fm = {};
  for (const line of m[1].split(/\r?\n/)) {
    const mm = line.match(/^([A-Za-z0-9_]+):\s*(.*)$/);
    if (mm) fm[mm[1]] = mm[2].trim().replace(/^["']|["']$/g, '');
  }
  return fm;
}
const xmlEsc = (s) => String(s).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');

const now = Date.now();
const articles = (fs.existsSync(DIR) ? fs.readdirSync(DIR) : [])
  .filter((f) => f.endsWith('.md'))
  .map((f) => {
    const fm = frontmatter(fs.readFileSync(path.join(DIR, f), 'utf8'));
    return { slug: f.replace(/\.md$/, ''), title: fm.title, pubDate: new Date(fm.pubDate), draft: fm.draft === 'true' };
  })
  .filter((a) => a.title && !a.draft && !isNaN(a.pubDate) && now - a.pubDate.getTime() < WINDOW_MS)
  .sort((a, b) => b.pubDate - a.pubDate);

const urls = articles.map((a) => `  <url>
    <loc>${SITE}/news/${a.slug}/</loc>
    <news:news>
      <news:publication>
        <news:name>${xmlEsc(PUBLICATION)}</news:name>
        <news:language>en</news:language>
      </news:publication>
      <news:publication_date>${a.pubDate.toISOString()}</news:publication_date>
      <news:title>${xmlEsc(a.title)}</news:title>
    </news:news>
  </url>`).join('\n');

const xml = `<?xml version="1.0" encoding="UTF-8"?>
<!-- Google News sitemap. Auto-generated by scripts/build-news-sitemap.js — lists
     only articles published in the last 48 hours. Declared in robots.txt. -->
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9"
        xmlns:news="http://www.google.com/schemas/sitemap-news/0.9">
${urls}
</urlset>
`;
fs.writeFileSync(OUT, xml);
console.log(`✓ news-sitemap.xml — ${articles.length} article(s) within 48h`);
